package Exercicio3;

public class Livro {
	
	private String titulo;
	private String autor;
	private int numeroDePaginas;
	
	
	public Livro(String tituloToSet, String autorToSet, int numeroDePaginasToSet) {
		this.titulo = tituloToSet;
		this.autor = autorToSet;
		this.numeroDePaginas = numeroDePaginasToSet;
		
	}
}
